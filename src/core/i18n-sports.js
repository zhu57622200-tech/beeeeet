function teamZh(name) {
  const key = String(name || '').trim()
  if (TEAM_ZH[key]) return TEAM_ZH[key]
  return Object.entries(TEAM_ZH).find(([team]) => key.endsWith(' ' + team))?.[1] || ''
}

// P1 由 event 标题/slug 解析世界杯玩法中文名；缺失时返回空串，由调用方降级英文。
export function playTypeZh(event) {
  const slug = String(event?.slug || '').toLowerCase()
  const rawTitle = String(event?.title || '')
  const title = rawTitle.toLowerCase()
  const text = `${slug} ${title}`
  const stageTitle =
    rawTitle.match(/^(.+?)\s+stage of elimination\b/i) ||
    rawTitle.match(/\bstage of elimination\s*[-:]\s*(.+)$/i)
  const group = text.match(/world-cup-group-([a-l])-winner/) || text.match(/group\s+([a-l])\s+winner/)
  const groupLast = text.match(/world-cup-group-([a-l])-last-place/) || text.match(/group\s+([a-l])\s+last place/)
  const conf = text.match(/furthest-advancing-(caf|afc|conmebol|uefa|concacaf|ofc)-nation/) || text.match(/furthest advancing (caf|afc|conmebol|uefa|concacaf|ofc) nation/)
  const confZh = {
    caf: '非洲',
    afc: '亚洲',
    conmebol: '南美',
    uefa: '欧洲',
    concacaf: '中北美',
    ofc: '大洋洲',
  }
  if (stageTitle) return translateEntityLocal(stageTitle[1].trim()) + ' 淘汰阶段'
  if (groupLast) return groupLast[1].toUpperCase() + ' 组垫底'
  if (group) return group[1].toUpperCase() + ' 组头名'
  if (text.includes('golden-ball') || text.includes('golden ball')) return '金球奖'
  if (text.includes('golden-glove') || text.includes('golden glove')) return '金手套(最佳门将)'
  if (text.includes('bronze-boot') || text.includes('bronze boot')) return '铜靴'
  if (text.includes('golden-boot') || text.includes('golden boot')) return '金靴'
  if (text.includes('most-goal-contributions') || text.includes('most goal contributions')) return '进球贡献榜'
  if (text.includes('most-assists') || text.includes('most assists')) return '助攻榜'
  if (conf) return '最远' + confZh[conf[1]] + '球队'
  // P1 Loop3 长尾玩法补全（小组第二/最差球队/东道主/零封/冠军组/三巨头）
  const groupSecond = text.match(/group[\s-]+([a-l])[\s-]+second[\s-]?place/)
  if (groupSecond) return groupSecond[1].toUpperCase() + ' 组第二'
  if (text.includes('worst-placed') || text.includes('worst placed')) {
    if (text.includes('host')) return '最差东道主'
    const w = text.match(/(caf|afc|conmebol|uefa|concacaf|ofc)/)
    return w ? '最差' + confZh[w[1]] + '球队' : '最差球队'
  }
  if (text.includes('furthest') && text.includes('host')) return '最远东道主'
  if (text.includes('non-host') && text.includes('concacaf')) return '最远中北美球队'
  if (text.includes('clean sheet')) return '最多零封(门将)'
  if (text.includes('group of champion')) return '冠军所在组'
  if (text.includes('neymar') && text.includes('messi')) return '梅西/C罗/内马尔谁走最远'
  if (text.includes('fair-play-award') || text.includes('fair play award')) return '公平竞赛奖'
  if (text.includes('advance-to-knockout') || text.includes('knockout stages')) return '出线'
  if (text.includes('round-of-16') || text.includes('round of 16')) return '晋级16强'
  if (text.includes('quarterfinal')) return '晋级8强'
  if (text.includes('semifinal')) return '晋级4强'
  if (text.includes('reach-final') || text.includes('reach final')) return '晋级决赛'
  if (text.includes('top-scorer-nation') || text.includes('top scorer nation') || text.includes('top-goalscorer') || text.includes('top goalscorer')) return '最佳射手国'
  if (text.includes('continent')) return '夺冠大洲'
  if (text.includes('winner')) return '夺冠'
  return ''
}

// P1 世界杯详情描述模板：覆盖来源英文长规则，只做前端展示文案。
export function playTypeDescZh(event) {
  const play = playTypeZh(event)
  if (play === '夺冠') return '押哪支球队夺得 2026 世界杯冠军。'
  if (play === '出线') return '押哪些球队能从小组出线进入淘汰赛。'
  if (play === '金靴') return '押谁是 2026 世界杯进球最多的金靴得主。'
  if (play.endsWith('组头名')) return `押 ${play.replace(' 组头名', '')} 组谁能拿到小组头名。`
  if (play.startsWith('晋级')) return `押哪些球队能晋级到 2026 世界杯 ${play.replace('晋级', '')}。`
  return play || String(event?.title || '')
}

// P1 候选实体名本地翻译：命中词典返中文，缺失降级原文。
export function translateEntityLocal(name) {
  const key = String(name || '').trim()
  if (key === 'Other') return '其他' // P1 榜单常见「Other」档（非热门候选归一）。
  if (/^Team [A-Z]+$/.test(key)) return '附加赛席位' // P1 世界杯附加赛未定席位占位符，只做展示归一。
  return COUNTRY_ZH[key] || PLAYER_ZH[key] || OUTCOME_ZH[key] || teamZh(key) || key
}
